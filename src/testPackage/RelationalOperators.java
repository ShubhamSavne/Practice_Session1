package testPackage;

public class RelationalOperators 
{
	public static void main(String[] args)
	{
	
	
		int a=50;
		int b=30;
		boolean result;
		
	//equal equal(==)
		result=(a==b);
		System.out.println(result);
		
	//greater than(>)
		result= a>b;
		System.out.println(result);
		
	//less than(<)
		result= a<b;
		System.out.println(result);
		
	
	//greater than and equal to(>=)
		result = a>=b;
		System.out.println(result);
		
	//less than and equal to(<=)
		result = a<=b;
		System.out.println(result);
	
	// not equal to(!=)
		result= a!=b;
		System.out.println(result);
		
	
	}	

}
